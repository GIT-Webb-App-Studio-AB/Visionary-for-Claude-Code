#!/usr/bin/env node
// Scans every style file's Colors section for hex codes, counts recurrences
// across styles, and writes skills/visionary/palette-tokens.md — a central
// registry of colors that appear in >= 3 styles. Lets generators reference a
// canonical token (`--visionary-color-saddle-brown`) instead of re-declaring
// the same hex in N files.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, basename, dirname, extname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const repoRoot = dirname(dirname(__filename));
const stylesDir = join(repoRoot, 'skills', 'visionary', 'styles');
const outputPath = join(repoRoot, 'skills', 'visionary', 'palette-tokens.md');

function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full));
    else if (entry.isFile() && extname(entry.name) === '.md' && entry.name !== '_index.md') out.push(full);
  }
  return out;
}

// Map of hex -> [styleIds, ...]
const hexMap = new Map();

for (const file of walk(stylesDir)) {
  const src = readFileSync(file, 'utf8');
  const id = basename(file, '.md');

  // Only scan the Colors section to avoid matching random hex in code comments.
  const colorsMatch = src.match(/##\s+Colors\s*\n([\s\S]*?)(?=\n##\s+|\n$)/);
  if (!colorsMatch) continue;

  const seen = new Set();
  for (const m of colorsMatch[1].matchAll(/#([0-9A-Fa-f]{6}|[0-9A-Fa-f]{3})\b/g)) {
    let hex = '#' + m[1].toUpperCase();
    // Expand 3-digit hex to 6-digit for consistent keying.
    if (hex.length === 4) {
      hex = '#' + hex.slice(1).split('').map((c) => c + c).join('').toUpperCase();
    }
    if (seen.has(hex)) continue;
    seen.add(hex);
    const arr = hexMap.get(hex) || [];
    arr.push(id);
    hexMap.set(hex, arr);
  }
}

// Take only colors that appear in >= 3 styles - those earn a token.
const shared = [...hexMap.entries()]
  .filter(([, styles]) => styles.length >= 3)
  .sort((a, b) => b[1].length - a[1].length);

// -- Hex -> OKLCH conversion (sRGB assumption) ----------------------------
function hexToOklch(hex) {
  const h = hex.replace('#', '');
  const r = parseInt(h.slice(0, 2), 16) / 255;
  const g = parseInt(h.slice(2, 4), 16) / 255;
  const b = parseInt(h.slice(4, 6), 16) / 255;

  // sRGB -> linear
  const lin = (v) => (v <= 0.04045 ? v / 12.92 : Math.pow((v + 0.055) / 1.055, 2.4));
  const rL = lin(r), gL = lin(g), bL = lin(b);

  // Linear sRGB -> OKLab (Bjorn Ottosson, 2020)
  const lMs = 0.4122214708 * rL + 0.5363325363 * gL + 0.0514459929 * bL;
  const mMs = 0.2119034982 * rL + 0.6806995451 * gL + 0.1073969566 * bL;
  const sMs = 0.0883024619 * rL + 0.2817188376 * gL + 0.6299787005 * bL;

  const lCubert = Math.cbrt(lMs);
  const mCubert = Math.cbrt(mMs);
  const sCubert = Math.cbrt(sMs);

  const L = 0.2104542553 * lCubert + 0.7936177850 * mCubert - 0.0040720468 * sCubert;
  const a = 1.9779984951 * lCubert - 2.4285922050 * mCubert + 0.4505937099 * sCubert;
  const b2 = 0.0259040371 * lCubert + 0.7827717662 * mCubert - 0.8086757660 * sCubert;

  const C = Math.sqrt(a * a + b2 * b2);
  let Hdeg = (Math.atan2(b2, a) * 180) / Math.PI;
  if (Hdeg < 0) Hdeg += 360;

  return `oklch(${L.toFixed(3)} ${C.toFixed(3)} ${Hdeg.toFixed(1)})`;
}

function deriveName(hex, styles) {
  const sid = styles[0];
  const [cat] = sid.split('-');
  return `${cat}-${sid.split('-').slice(-1)[0]}`;
}

// -- Emit markdown --------------------------------------------------------
const lines = [];
lines.push('# Palette Tokens - Cross-Style Color Registry');
lines.push('');
lines.push('Auto-generated by `scripts/build-palette-tokens.mjs`. Lists every hex that');
lines.push('appears in the **Colors** section of 3 or more style files. Generators should');
lines.push('reference these via `var(--visionary-color-*)` instead of hardcoding the same');
lines.push('hex repeatedly.');
lines.push('');
lines.push('OKLCH values are the perceptually-uniform equivalent (CSS Color 4, Baseline');
lines.push('2024). Prefer OKLCH for interpolation, color-mix(), and wide-gamut output.');
lines.push('');
lines.push('## How to use in generated components');
lines.push('');
lines.push('```css');
lines.push(':root {');
for (const [hex, styles] of shared.slice(0, 5)) {
  const name = deriveName(hex, styles);
  lines.push(`  --visionary-color-${name}: ${hex}; /* ${hexToOklch(hex)} */`);
}
lines.push('}');
lines.push('```');
lines.push('');
lines.push(`## Shared Colors (${shared.length})`);
lines.push('');
lines.push('| Hex | OKLCH | Styles | Count |');
lines.push('|---|---|---|---|');

for (const [hex, styles] of shared) {
  const oklch = hexToOklch(hex);
  const styleList = styles.length > 5
    ? styles.slice(0, 5).join(', ') + `, ...(+${styles.length - 5})`
    : styles.join(', ');
  lines.push(`| \`${hex}\` | \`${oklch}\` | ${styleList} | ${styles.length} |`);
}

lines.push('');
lines.push(`## Total unique colors scanned: ${hexMap.size}`);
lines.push(`Unique colors appearing in >=3 styles: **${shared.length}**`);
lines.push('');
lines.push('---');
lines.push('');
lines.push('_Re-run: `node scripts/build-palette-tokens.mjs`_');

writeFileSync(outputPath, lines.join('\n') + '\n', 'utf8');
console.log(`scanned styles: ${hexMap.size} unique colors`);
console.log(`shared (>=3 styles): ${shared.length}`);
console.log(`wrote: ${outputPath}`);
