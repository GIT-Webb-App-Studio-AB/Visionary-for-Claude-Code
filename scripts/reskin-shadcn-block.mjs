#!/usr/bin/env node
// shadcn block re-skinner. Consumes a shadcn registry block (or the shadcn/ui
// catalog's exported blocks) and re-skins it with a Visionary style's tokens.
//
// The idea: instead of competing with shadcn/ui's 1 000+ community blocks,
// we consume them and re-skin. User writes a dashboard with `npx shadcn add
// dashboard-01`, then we transform the imported CSS variables + class usage
// so the block feels like `swiss-rationalism` / `cassette-futurism` / etc.
//
// Usage:
//   node scripts/reskin-shadcn-block.mjs \
//     --block ./src/components/blocks/dashboard-01.tsx \
//     --style cassette-futurism \
//     --out ./src/components/blocks/dashboard-01.reskinned.tsx
//
// The transform is non-destructive — original stays put, re-skinned is a
// sibling. Diff them to review before replacing.

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname, basename, extname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { argv } from 'node:process';

const __filename = fileURLToPath(import.meta.url);
const repoRoot = dirname(dirname(__filename));
const tokensDir = join(repoRoot, 'tokens');

function flag(name) {
  const i = argv.indexOf(`--${name}`);
  return i === -1 ? null : argv[i + 1];
}

const blockPath = flag('block');
const styleId = flag('style');
const outPath = flag('out') || (blockPath && blockPath.replace(/(\.\w+)$/, '.reskinned$1'));

if (!blockPath || !styleId) {
  console.error('usage: reskin-shadcn-block.mjs --block FILE --style STYLE_ID [--out FILE]');
  process.exit(1);
}

if (!existsSync(blockPath)) {
  console.error(`block not found: ${blockPath}`);
  process.exit(1);
}

const tokensPath = join(tokensDir, `${styleId}.tokens.json`);
if (!existsSync(tokensPath)) {
  console.error(`style not found: ${tokensPath}`);
  console.error('Run `node scripts/export-dtcg-tokens.mjs` if tokens/ is stale.');
  process.exit(1);
}

const tokens = JSON.parse(readFileSync(tokensPath, 'utf8'));
const src = readFileSync(blockPath, 'utf8');

// ── Transform rules ──────────────────────────────────────────────────────────
// Non-exhaustive — covers the most common shadcn patterns. Extend as needed.
// The rules REPLACE Tailwind classes / CSS variables with the style's tokens.

const bg     = tokens.color?.bg?.$value;
const fg     = tokens.color?.text?.$value;
const prim   = tokens.color?.primary?.$value;
const accent = tokens.color?.accent?.$value;
const font   = tokens.typography?.display?.$value;
const radius = tokens.spacing?.['radius-min']?.$value || tokens.spacing?.radius?.$value;
const tier   = tokens.motion?.tier?.$value || 'Subtle';

// Every replacement is a pair of [regex, replacement]. Order matters —
// more specific patterns first.
const transforms = [
  // 1. shadcn's CSS-variable references → our tokens via @theme
  [/hsl\(\s*var\(--background\)\s*\)/g, bg || 'var(--visionary-bg)'],
  [/hsl\(\s*var\(--foreground\)\s*\)/g, fg || 'var(--visionary-fg)'],
  [/hsl\(\s*var\(--primary\)\s*\)/g,     prim || 'var(--visionary-primary)'],
  [/hsl\(\s*var\(--accent\)\s*\)/g,      accent || 'var(--visionary-accent)'],

  // 2. Tailwind class replacements (only hit the obvious ones; leave the
  //    rest for the user to review)
  [/\bbg-background\b/g,  `[background-color:${bg}]`],
  [/\btext-foreground\b/g, `[color:${fg}]`],
  [/\bbg-primary\b/g,     `[background-color:${prim}]`],
  [/\btext-primary\b/g,   `[color:${prim}]`],
  [/\bbg-accent\b/g,      `[background-color:${accent}]`],

  // 3. Border-radius — replace shadcn's default `rounded-lg` with the
  //    style's radius vocabulary
  radius === '0px'
    ? [/\brounded-(lg|md|sm|xl)\b/g, 'rounded-none']
    : [/\brounded-lg\b/g, `[border-radius:${radius}]`],

  // 4. Font-family — if the block declares Inter/Geist, swap for the
  //    style's display font
  font ? [/font-family:\s*['"]?(Inter|Geist|Roboto)[^;"']*['"]?/g, `font-family:"${font.split(',')[0].trim()}"`] : null,

  // 5. Motion tier enforcement — if the style is Static, strip all
  //    `transition` and `animate-*` classes
  tier === 'Static'
    ? [/\b(transition|transition-all|transition-[a-z]+|animate-[a-z]+|duration-\d+)\b/g, '']
    : null,

  // 6. Shadow adjustment — if the style has no shadow budget, strip
  ['shadow-none', 'shadow-md', 'shadow-sm'].includes(undefined)
    ? null
    : null,
].filter(Boolean);

// Apply
let out = src;
let replacementsMade = 0;
for (const [re, repl] of transforms) {
  const before = out;
  out = out.replace(re, typeof repl === 'function' ? repl : repl);
  if (out !== before) replacementsMade++;
}

// Inject a header comment so reviewers know what happened
const header = `// Auto-generated by scripts/reskin-shadcn-block.mjs
// Source block:  ${basename(blockPath)}
// Visionary style: ${styleId}
// Motion tier:   ${tier}
// Transforms applied: ${replacementsMade}
//
// Review before replacing the original. Delete this comment when accepted.

`;

writeFileSync(outPath, header + out, 'utf8');

console.log(`Re-skinned ${basename(blockPath)} → ${basename(outPath)}`);
console.log(`  style:        ${styleId}`);
console.log(`  transforms:   ${replacementsMade}`);
console.log(`  review with:  git diff --no-index ${blockPath} ${outPath}`);
